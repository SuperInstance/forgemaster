def emit_llvm_ir(constraint, func_name="flux_check", vectorize=False, width=16):
    """Emit LLVM IR for a constraint.
    
    Theorem 7 (DeepSeek): Pipeline correctness preserved through each stage.
    This emitter produces IR that LLVM then optimizes for the target.
    """
    lines = [
        '; FLUX Constraint → LLVM IR',
        '; Generated by flux_llvm_backend.py',
        ';',
        '; Optimization passes applied by LLVM:',
        ';   - dead constraint elimination (Theorem 5)',
        ';   - strength reduction (Theorem 6: range [0,2^k-1] → mask)',
        ';   - SIMD vectorization (Theorem 4: mask semantics preserved)',
        ';   - optimal instruction selection (Theorem 3)',
        '',
        'target datalayout = "e-m:e-p270:32:32-p271:32:32-p272:64:64-i64:64-f80:128-n8:16:32:64-S128"',
        'target triple = "x86_64-pc-linux-gnu"',
        '',
    ]
    
    if vectorize:
        return emit_llvm_ir_vectorized(constraint, func_name, width)
    
    # Scalar function: (i32) -> i1
    lines.append(f'define i1 @{func_name}(i32 %val) {{')
    
    if isinstance(constraint, RangeConstraint):
        # Strength reduction: if lo=0, use unsigned comparison (1 cmp instead of 2)
        if constraint.lo == 0:
            lines.append(f'  ; Range [0, {constraint.hi}] → unsigned ≤ {constraint.hi}')
            lines.append(f'  %cmp = icmp ule i32 %val, {constraint.hi}')
            lines.append(f'  ret i1 %cmp')
        else:
            # General range: (unsigned)(val - lo) <= (hi - lo)
            lines.append(f'  ; Range [{constraint.lo}, {constraint.hi}] → subtraction trick')
            lines.append(f'  %off = sub i32 %val, {constraint.lo}')
            lines.append(f'  %span = sub i32 {constraint.hi}, {constraint.lo}')
            lines.append(f'  %cmp = icmp ule i32 %off, %span')
            lines.append(f'  ret i1 %cmp')
    
    elif isinstance(constraint, DomainConstraint):
        # (val & ~mask) == 0  ↔  (val & mask) == val
        complement = (~constraint.mask) & 0xFFFFFFFF
        lines.append(f'  ; Domain check: (val & 0x{constraint.mask:X}) == val')
        lines.append(f'  %masked = and i32 %val, {complement}')
        lines.append(f'  %cmp = icmp eq i32 %masked, 0')
        lines.append(f'  ret i1 %cmp')
    
    elif isinstance(constraint, AndConstraint):
        ranges = [c for c in constraint.constraints if isinstance(c, RangeConstraint)]
        domains = [c for c in constraint.constraints if isinstance(c, DomainConstraint)]
        
        # Dead constraint elimination (Theorem 5):
        # If domain mask ⊂ range bounds, range is dead
        for rc in ranges[:]:
            if rc.lo == 0:
                # Check if any domain mask implies this range
                for dc in domains:
                    if (dc.mask & rc.hi) == rc.hi:
                        ranges.remove(rc)
                        lines.append(f'  ; DEAD ELIMINATED: range [{rc.lo},{rc.hi}] subsumed by domain 0x{dc.mask:X}')
                        break
        
        result_var = 0
        for idx, rc in enumerate(ranges):
            if rc.lo == 0:
                lines.append(f'  %r{idx} = icmp ule i32 %val, {rc.hi}')
            else:
                lines.append(f'  %off{idx} = sub i32 %val, {rc.lo}')
                lines.append(f'  %r{idx} = icmp ule i32 %off{idx}, {rc.hi - rc.lo}')
            result_var = idx
        
        for idx, dc in enumerate(domains):
            complement = (~dc.mask) & 0xFFFFFFFF
            lines.append(f'  %dm{idx} = and i32 %val, {complement}')
            lines.append(f'  %d{idx} = icmp eq i32 %dm{idx}, 0')
        
        # AND all results together
        all_conds = [f'%r{i}' for i in range(len(ranges))] + [f'%d{i}' for i in range(len(domains))]
        if len(all_conds) == 0:
            lines.append(f'  ret i1 true')
        elif len(all_conds) == 1:
            lines.append(f'  ret i1 {all_conds[0]}')
        else:
            prev = all_conds[0]
            for i in range(1, len(all_conds)):
                lines.append(f'  %and{i} = and i1 {prev}, {all_conds[i]}')
                prev = f'%and{i}'
            lines.append(f'  ret i1 {prev}')
    
    lines.append('}')
    return '\n'.join(lines)


def emit_llvm_ir_vectorized(constraint, func_name="flux_check_batch", width=16):
    """Emit LLVM IR with explicit SIMD vectorization.
    
    Theorem 4 (DeepSeek/Qwen): SIMD evaluation produces identical results to scalar.
    Proof by induction: kand preserves mask semantics at each step.
    """
    lines = [
        f'; FLUX Vectorized Constraint → LLVM IR ({width}-wide SIMD)',
        'target datalayout = "e-m:e-p270:32:32-p271:32:32-p272:64:64-i64:64-f80:128-n8:16:32:64-S128"',
        'target triple = "x86_64-pc-linux-gnu"',
        '',
        f'define <{width} x i1> @{func_name}(<{width} x i32> %val) {{',
    ]
    
    if isinstance(constraint, RangeConstraint):
        if constraint.lo == 0:
            lines.append(f'  %broadcast_hi = insertelement <{width} x i32> undef, i32 {constraint.hi}, i32 0')
            lines.append(f'  %hi = shufflevector <{width} x i32> %broadcast_hi, <{width} x i32> undef, <{width} x i32> zeroinitializer')
            lines.append(f'  %cmp = icmp ule <{width} x i32> %val, %hi')
            lines.append(f'  ret <{width} x i1> %cmp')
        else:
            lines.append(f'  %broadcast_lo = insertelement <{width} x i32> undef, i32 {constraint.lo}, i32 0')
            lines.append(f'  %lo = shufflevector <{width} x i32> %broadcast_lo, <{width} x i32> undef, <{width} x i32> zeroinitializer')
            lines.append(f'  %off = sub <{width} x i32> %val, %lo')
            lines.append(f'  %span = insertelement <{width} x i32> undef, i32 {constraint.hi - constraint.lo}, i32 0')
            lines.append(f'  %span_v = shufflevector <{width} x i32> %span, <{width} x i32> undef, <{width} x i32> zeroinitializer')
            lines.append(f'  %cmp = icmp ule <{width} x i32> %off, %span_v')
            lines.append(f'  ret <{width} x i1> %cmp')
    
    elif isinstance(constraint, AndConstraint):
        ranges = [c for c in constraint.constraints if isinstance(c, RangeConstraint)]
        results = []
        
        for idx, rc in enumerate(ranges):
            if rc.lo == 0:
                lines.append(f'  %hi{idx} = insertelement <{width} x i32> undef, i32 {rc.hi}, i32 0')
                lines.append(f'  %hi_v{idx} = shufflevector <{width} x i32> %hi{idx}, <{width} x i32> undef, <{width} x i32> zeroinitializer')
                lines.append(f'  %cmp{idx} = icmp ule <{width} x i32> %val, %hi_v{idx}')
            else:
                lines.append(f'  %lo{idx} = insertelement <{width} x i32> undef, i32 {rc.lo}, i32 0')
                lines.append(f'  %lo_v{idx} = shufflevector <{width} x i32> %lo{idx}, <{width} x i32> undef, <{width} x i32> zeroinitializer')
                lines.append(f'  %off{idx} = sub <{width} x i32> %val, %lo_v{idx}')
                lines.append(f'  %span{idx} = insertelement <{width} x i32> undef, i32 {rc.hi - rc.lo}, i32 0')
                lines.append(f'  %span_v{idx} = shufflevector <{width} x i32> %span{idx}, <{width} x i32> undef, <{width} x i32> zeroinitializer')
                lines.append(f'  %cmp{idx} = icmp ule <{width} x i32> %off{idx}, %span_v{idx}')
            results.append(f'%cmp{idx}')
        
        if len(results) == 1:
            lines.append(f'  ret <{width} x i1> {results[0]}')
        else:
            prev = results[0]
            for i in range(1, len(results)):
                lines.append(f'  %and{i} = and <{width} x i1> {prev}, {results[i]}')
                prev = f'%and{i}'
            lines.append(f'  ret <{width} x i1> {prev}')
    
    lines.append('}')
    return '\n'.join(lines)


def emit_llvm_ir_batch(func_name="flux_check_batch_c", width=16):
    """Emit the C-callable batch wrapper that processes arrays."""
    return f'''
; C-callable batch constraint check
; void flux_check_batch(const int32_t* input, int32_t* output, int64_t n)
define void @{func_name}(i32* %input, i32* %output, i64 %n) {{
entry:
  %input.addr = alloca i32*
  %output.addr = alloca i32*
  %n.addr = alloca i64
  %i = alloca i64
  store i32* %input, i32** %input.addr
  store i32* %output, i32** %output.addr
  store i64 %n, i64* %n.addr
  store i64 0, i64* %i
  br label %loop.cond

loop.cond:
  %i.val = load i64, i64* %i
  %n.val = load i64, i64* %n.addr
  %cmp = icmp slt i64 %i.val, %n.val
  br i1 %cmp, label %loop.body, label %loop.end

loop.body:
  ; This is a placeholder — the actual constraint check is inlined here
  ; by the compiler when it specializes this template for a specific constraint
  %i.val2 = load i64, i64* %i
  %in.ptr = load i32*, i32** %input.addr
  %in.gep = getelementptr i32, i32* %in.ptr, i64 %i.val2
  %val = load i32, i32* %in.gep
  
  ; CONSTRAINT CHECK PLACEHOLDER — replaced by specialization
  ; For range [0, 100]: icmp ule i32 %val, 100
  %check = icmp ule i32 %val, 100
  
  %result = zext i1 %check to i32
  %out.ptr = load i32*, i32** %output.addr
  %out.gep = getelementptr i32, i32* %out.ptr, i64 %i.val2
  store i32 %result, i32* %out.gep
  
  %i.next = add i64 %i.val2, 1
  store i64 %i.next, i64* %i
  br label %loop.cond

loop.end:
  ret void
}}
'''


