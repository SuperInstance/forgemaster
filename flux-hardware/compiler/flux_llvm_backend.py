def emit_ebpf(constraint, func_name="flux_check_ebpf"):
    """Emit eBPF C code for constraint checking.
    
    eBPF = free formal verification. The kernel verifier mathematically proves:
    - No crashes (bounds checked)
    - No infinite loops (bounded iteration)
    - No OOB access (verified memory access)
    
    This is a constraint checker the Linux kernel ITSELF verifies is correct.
    """
    lines = [
        '#include <linux/bpf.h>',
        '#include <bpf/bpf_helpers.h>',
        '',
        f'SEC("socket")',
        f'int {func_name}(struct __sk_buff *skb) {{',
        '    // eBPF constraint checker',
        '    // The kernel verifier proves this program:',
        '    //   1. Terminates (bounded loop)',
        '    //   2. No OOB access (verified bounds)',
        '    //   3. No crashes (type-checked)',
        '',
        '    // Extract value from packet (example: byte at offset 0)',
        '    __u32 val;',
        '    bpf_skb_load_bytes(skb, 0, &val, sizeof(val));',
        '',
    ]
    
    if isinstance(constraint, RangeConstraint):
        if constraint.lo == 0:
            lines.append(f'    // Range [0, {constraint.hi}] → unsigned comparison')
            lines.append(f'    if (val > {constraint.hi}) return 0;  // DROP')
        else:
            lines.append(f'    // Range [{constraint.lo}, {constraint.hi}]')
            lines.append(f'    if (val < {constraint.lo} || val > {constraint.hi}) return 0;  // DROP')
    
    elif isinstance(constraint, DomainConstraint):
        complement = (~constraint.mask) & 0xFFFFFFFF
        lines.append(f'    // Domain check: (val & 0x{constraint.mask:X}) == val')
        lines.append(f'    if (val & 0x{complement:X}) return 0;  // DROP')
    
    elif isinstance(constraint, AndConstraint):
        for c in constraint.constraints:
            if isinstance(c, RangeConstraint):
                lines.append(f'    if (val < {c.lo} || val > {c.hi}) return 0;  // DROP')
            elif isinstance(c, DomainConstraint):
                complement = (~c.mask) & 0xFFFFFFFF
                lines.append(f'    if (val & 0x{complement:X}) return 0;  // DROP')
    
    lines.append('')
    lines.append('    return 1;  // PASS')
    lines.append('}')
    lines.append('')
    lines.append('char _license[] SEC("license") = "Apache-2.0";')
    return '\n'.join(lines)


def emit_wasm(constraint, func_name="flux_check_wasm"):
    """Emit WAT (WebAssembly Text) for browser-based constraint checking."""
    lines = [
        '(module',
        f'  (func ${func_name} (param i32) (result i32)',
    ]
    
    if isinstance(constraint, RangeConstraint):
        if constraint.lo == 0:
            # Strength reduction: unsigned ≤ hi
            lines.append(f'    local.get 0')
            lines.append(f'    i32.const {constraint.hi}')
            lines.append(f'    i32.le_u')
        else:
            # Subtraction trick
            lines.append(f'    local.get 0')
            lines.append(f'    i32.const {constraint.lo}')
            lines.append(f'    i32.sub')
            lines.append(f'    i32.const {constraint.hi - constraint.lo}')
            lines.append(f'    i32.le_u')
    
    elif isinstance(constraint, DomainConstraint):
        complement = (~constraint.mask) & 0xFFFFFFFF
        lines.append(f'    local.get 0')
        lines.append(f'    i32.const {complement}')
        lines.append(f'    i32.and')
        lines.append(f'    i32.eqz')
    
    elif isinstance(constraint, AndConstraint):
        ranges = [c for c in constraint.constraints if isinstance(c, RangeConstraint)]
        for i, rc in enumerate(ranges):
            if rc.lo == 0:
                lines.append(f'    local.get 0')
                lines.append(f'    i32.const {rc.hi}')
                lines.append(f'    i32.le_u')
            else:
                lines.append(f'    local.get 0')
                lines.append(f'    i32.const {rc.lo}')
                lines.append(f'    i32.sub')
                lines.append(f'    i32.const {rc.hi - rc.lo}')
                lines.append(f'    i32.le_u')
            if i > 0:
                lines.append(f'    i32.and')
    
    lines.append(f'  )')
    lines.append(f'  (export "{func_name}" (func ${func_name}))')
    lines.append(')')
    return '\n'.join(lines)


def emit_riscv(constraint, func_name="flux_check_riscv"):
    """Emit RISC-V assembly (RV32I base + Xconstr extension).
    
    Xconstr extension adds custom instructions for constraint checking:
    - CREVISE: Constraint range check (hardware instruction)
    - CMASK: Domain mask check (hardware instruction)
    """
    lines = [
        f'# FLUX Constraint → RISC-V + Xconstr',
        f'# Function: {func_name}',
        f'# Input: a0 = value to check',
        f'# Output: a0 = 1 (pass) or 0 (fail)',
        '',
        f'.globl {func_name}',
        f'{func_name}:',
    ]
    
    if isinstance(constraint, RangeConstraint):
        if constraint.lo == 0:
            # li a1, hi; sltu a0, a1, a0; xori a0, a0, 1 → 3 instructions
            if constraint.hi < 4096:
                lines.append(f'    li      a1, {constraint.hi}')
            else:
                lines.append(f'    lui     a1, %hi({constraint.hi})')
                lines.append(f'    addi    a1, a1, %lo({constraint.hi})')
            lines.append(f'    sltu    a0, a1, a0      # a0 = (hi < val) ? 1 : 0')
            lines.append(f'    xori    a0, a0, 1       # invert: a0 = (val <= hi) ? 1 : 0')
        else:
            # Subtraction trick for [lo, hi]
            lines.append(f'    addi    a0, a0, -{constraint.lo}    # a0 = val - lo')
            lines.append(f'    li      a1, {constraint.hi - constraint.lo}')
            lines.append(f'    sltu    a0, a1, a0              # (span < offset)?')
            lines.append(f'    xori    a0, a0, 1                # invert')
            # Xconstr: could be single CREVISE lo,hi instruction
    
    elif isinstance(constraint, DomainConstraint):
        complement = (~constraint.mask) & 0xFFFFFFFF
        lines.append(f'    li      a1, {complement}')
        lines.append(f'    and     a0, a0, a1       # a0 = val & ~mask')
        lines.append(f'    sltiu   a0, a0, 1        # a0 = (result == 0) ? 1 : 0')
        # Xconstr: single CMASK mask instruction
    
    lines.append(f'    ret')
    return '\n'.join(lines)


