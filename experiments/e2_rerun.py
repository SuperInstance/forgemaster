    lines.append(f"| V | γ (mean±std) | H (mean±std) | γ+H (mean±std) | Predicted | Residual |")
    lines.append(f"|---|-------------|-------------|----------------|-----------|----------|")
    
    for V in FLEET_SIZES:
        r = results[V]
        lines.append(
            f"| {V} | {r['mean_gamma']:.4f}±{r['std_gamma']:.4f} "
            f"| {r['mean_H']:.4f}±{r['std_H']:.4f} "
            f"| {r['mean_gamma_plus_H']:.4f}±{r['std_gamma_plus_H']:.4f} "
            f"| {r['predicted']:.4f} | {r['residual']:+.4f} |"
        )
    
    # Regression fit
    Vs = np.array(FLEET_SIZES, dtype=float)
    lnV = np.log(Vs)
    gH_means = np.array([results[V]["mean_gamma_plus_H"] for V in FLEET_SIZES])
    
    # Linear regression: γ+H = a + b·ln(V)
    A = np.vstack([np.ones_like(lnV), lnV]).T
    a_fit, b_fit = np.linalg.lstsq(A, gH_means, rcond=None)[0]
    
    gamma_means = np.array([results[V]["mean_gamma"] for V in FLEET_SIZES])
    H_means = np.array([results[V]["mean_H"] for V in FLEET_SIZES])
    
    # γ regression: γ = a_γ + b_γ·ln(V)
    a_g, b_g = np.linalg.lstsq(A, gamma_means, rcond=None)[0]
    # H regression
    a_h, b_h = np.linalg.lstsq(A, H_means, rcond=None)[0]
    
    lines.append(f"")
    lines.append(f"## Empirical Fits")
    lines.append(f"")
    lines.append(f"**γ+H fit:** {a_fit:.3f} {b_fit:+.3f}·ln(V)")
    lines.append(f"**Predicted:** 1.283 − 0.159·ln(V)")
    lines.append(f"**γ fit:** {a_g:.3f} {b_g:+.3f}·ln(V)")
    lines.append(f"**H fit:** {a_h:.3f} {b_h:+.3f}·ln(V)")
    lines.append(f"")
    
    # Key findings
    lines.append(f"## Key Findings")
    lines.append(f"")
    
    # Does γ→0?
    if abs(b_g) > 0.01 and b_g < 0:
        lines.append(f"- **γ→0 as V grows:** YES (slope = {b_g:.3f})")
    elif abs(b_g) > 0.01:
        lines.append(f"- **γ→0 as V grows:** NO (slope = {b_g:+.3f}, positive)")
    else:
        lines.append(f"- **γ→0 as V grows:** INCONCLUSIVE (slope ≈ 0)")
    
    # Does log-linear form emerge?
    residuals = [results[V]["residual"] for V in FLEET_SIZES]
    rmse = np.sqrt(np.mean(np.array(residuals)**2))
    lines.append(f"- **Log-linear form:** RMSE from predicted = {rmse:.4f}")
    if rmse < 0.1:
        lines.append(f"  - **Strong fit** — conservation law holds with more rounds")
    elif rmse < 0.2:
        lines.append(f"  - **Moderate fit** — trend present but noisy")
    else:
        lines.append(f"  - **Weak fit** — conservation law may not hold exactly")
    
    # γ+H stability
    gH_stds = [results[V]["std_gamma_plus_H"] for V in FLEET_SIZES]
    lines.append(f"- **γ+H stability (std):** {', '.join(f'{s:.4f}' for s in gH_stds)}")
    lines.append(f"- **Mean std:** {np.mean(gH_stds):.4f}")
    
    lines.append(f"")
    lines.append(f"## Per-Round Data")
    lines.append(f"")
    
    for V in FLEET_SIZES:
        r = results[V]
        lines.append(f"### V={V}")
