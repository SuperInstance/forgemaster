"""
understanding_sheaf.py — Sheaf construction for neural network models

Constructs a sheaf over models that share representation layers.
The sheaf assigns to each "open set" (model or group of models) the
understanding they have of the data. Restriction maps project understanding
from larger groups to subsets.

Topology: Alexandrov topology on the partial order of model coverage.
  - Open sets = upward-closed sets in the coverage order
  - Two models "cover" a region if their representations are compatible there

The key question: can local understandings be glued into global understanding?
H⁰ = space of global sections (global understanding exists)
H¹ = obstruction to gluing (local pieces don't fit together)
"""

import numpy as np
from dataclasses import dataclass, field
from typing import List, Dict, Tuple, Optional, Callable
from itertools import combinations


@dataclass
class ModelRepresentation:
    """A model's representation of data points."""
    name: str
    representations: np.ndarray  # (n_data, embed_dim)
    coverage: np.ndarray         # boolean mask (n_data,) — which points this model "sees"

    @property
    def embed_dim(self) -> int:
        return self.representations.shape[1]

    @property
    def n_data(self) -> int:
        return self.representations.shape[0]


class AlexandrovTopology:
    """
    Alexandrov topology from the coverage partial order on models.

    Two models A, B are "compatible" if their representations agree
    on the overlap of their coverage regions (within tolerance ε).

    The open sets are generated by the upward closure of singletons
    in the compatibility order.
    """

    def __init__(self, tolerance: float = 0.5):
        self.tolerance = tolerance
        self.compatibility_matrix: Optional[np.ndarray] = None

    def compute_compatibility(
        self,
        models: List[ModelRepresentation],
    ) -> np.ndarray:
        """
        Compute pairwise compatibility between models.

        Two models are compatible if their representations agree
        on shared data points (within tolerance).

        Returns: (n_models, n_models) boolean matrix
        """
        n = len(models)
        compat = np.zeros((n, n), dtype=bool)

        for i in range(n):
            compat[i, i] = True  # self-compatible
            for j in range(i + 1, n):
                # Find shared coverage
                shared = models[i].coverage & models[j].coverage
                if shared.sum() < 2:
                    # No significant overlap — check with relaxed criterion
                    compat[i, j] = False
                    compat[j, i] = False
                    continue

                # Compare representations on shared points
                rep_i = models[i].representations[shared]
                rep_j = models[j].representations[shared]

                # Project to common dimensionality (use PCA-like projection)
                dim = min(rep_i.shape[1], rep_j.shape[1])
                rep_i_proj = rep_i[:, :dim]
                rep_j_proj = rep_j[:, :dim]

                # Cosine similarity on shared points
                norms_i = np.linalg.norm(rep_i_proj, axis=1, keepdims=True)
                norms_j = np.linalg.norm(rep_j_proj, axis=1, keepdims=True)
                norms_i = np.maximum(norms_i, 1e-10)
                norms_j = np.maximum(norms_j, 1e-10)

                cos_sim = np.sum(
                    rep_i_proj / norms_i * rep_j_proj / norms_j, axis=1
                )
                mean_sim = cos_sim.mean()

                compatible = mean_sim > (1.0 - self.tolerance)
                compat[i, j] = compatible
                compat[j, i] = compatible

        self.compatibility_matrix = compat
        return compat

    def get_open_sets(self, n_models: int) -> List[frozenset]:
        """
        Generate open sets of the Alexandrov topology.

        In the Alexandrov topology from a preorder, open sets are
        upward-closed sets. For compatibility, we generate:
        1. The full set {all models}
        2. Individual models {i}
        3. Compatible pairs {i, j} where compatible[i,j]
        4. Compatible triples, etc.
        """
        if self.compatibility_matrix is None:
            raise ValueError("Must call compute_compatibility first")

        opens = [frozenset(range(n_models))]  # full set

        # Singletons are always open
        for i in range(n_models):
            opens.append(frozenset([i]))

        # Pairs
        for i, j in combinations(range(n_models), 2):
            if self.compatibility_matrix[i, j]:
                opens.append(frozenset([i, j]))

        # Triples (all pairwise compatible)
        for triple in combinations(range(n_models), 3):
            i, j, k = triple
            if (self.compatibility_matrix[i, j] and
                self.compatibility_matrix[i, k] and
                self.compatibility_matrix[j, k]):
                opens.append(frozenset(triple))

        # Remove duplicates
        opens = list(set(opens))
        return opens


class UnderstandingSheaf:
    """
    Sheaf of understanding over models.

    For each open set U of models, F(U) is the vector space of
    "consistent understandings" that all models in U agree on.

    Restriction maps: F(U) → F(V) for V ⊆ U project the shared
    understanding to a smaller group.

    We represent sections as vectors in a shared embedding space.
    """

    def __init__(
        self,
        models: List[ModelRepresentation],
        topology: AlexandrovTopology,
    ):
        self.models = models
        self.topology = topology
        self.sections: Dict[frozenset, np.ndarray] = {}
        self.restriction_maps: Dict[Tuple[frozenset, frozenset], np.ndarray] = {}

    def compute_sections(self):
        """
        Compute sections F(U) for each open set U.

        For a set of models U, the section is the mean representation
        on the shared coverage, projected to a common space.
        """
        # Determine common embedding dimension
        min_dim = min(m.embed_dim for m in self.models)

        for open_set in self.topology.get_open_sets(len(self.models)):
            if len(open_set) == 0:
                continue

            # Get models in this open set
            set_models = [self.models[i] for i in open_set]

            # Find shared coverage
            shared_coverage = np.ones(self.models[0].n_data, dtype=bool)
            for m in set_models:
                shared_coverage &= m.coverage

            if shared_coverage.sum() == 0:
                # No shared coverage — section is zero
                self.sections[open_set] = np.zeros(min_dim)
                continue

            # Compute section as mean representation on shared coverage
            reps = []
            for m in set_models:
                rep = m.representations[shared_coverage][:, :min_dim]
                reps.append(rep)

            # Section = mean across models
            section = np.mean(reps, axis=0).mean(axis=0)  # (min_dim,)
            self.sections[open_set] = section

    def compute_restriction_maps(self):
        """
        Compute restriction maps ρ_V^U: F(U) → F(V) for V ⊆ U.

        For our sheaf, restriction is projection: the section for U
        restricted to V is just the section for V.
        """
        opens = self.topology.get_open_sets(len(self.models))

        for u in opens:
            for v in opens:
                if v.issubset(u) and u != v:
                    # Restriction map: identity in shared space
                    dim = len(self.sections.get(u, np.zeros(1)))
                    self.restriction_maps[(u, v)] = np.eye(dim)

    def build(self):
        """Build the complete sheaf."""
        self.compute_sections()
        self.compute_restriction_maps()
        return self

    def get_section(self, open_set: frozenset) -> np.ndarray:
        """Get the section F(U) for open set U."""
        return self.sections.get(open_set, np.zeros(1))

    def get_cochain_data(self) -> Dict:
        """
        Get data needed for cohomology computation.

        Returns:
          - 0_cochains: sections for each open set (for H⁰)
          - 1_cochains: compatibility on overlaps (for H¹)
        """
        opens = self.topology.get_open_sets(len(self.models))

        # 0-cochains: assignments of vectors to open sets
        zero_cochains = {}
        for o in opens:
            if o in self.sections:
                zero_cochains[o] = self.sections[o]

        # 1-cochains: differences on pairwise overlaps
        # δ{s}(U ∩ V) = s(V)|_{U∩V} - s(U)|_{U∩V}
        one_cochains = {}
        for i, u in enumerate(opens):
            for j, v in enumerate(opens):
                if i < j and u != v:
                    overlap = u & v
                    if overlap:
                        su = self.sections.get(u, None)
                        sv = self.sections.get(v, None)
                        if su is not None and sv is not None:
                            # Coboundary: difference of sections
                            diff = sv[:len(su)] - su[:len(sv)] if len(sv) >= len(su) else sv - su[:len(sv)]
                            one_cochains[(u, v)] = diff

        return {
            "opens": opens,
            "zero_cochains": zero_cochains,
            "one_cochains": one_cochains,
        }


def create_model_pair(
    name_a: str,
    name_b: str,
    n_data: int = 100,
    embed_dim_a: int = 8,
    embed_dim_b: int = 8,
    compatible: bool = True,
    noise_level: float = 0.1,
    seed: int = 42,
) -> Tuple[ModelRepresentation, ModelRepresentation]:
    """
    Create a pair of models with controlled compatibility.

    If compatible=True, models have similar representations on shared data.
    If compatible=False, models disagree on shared data.
    """
    rng = np.random.RandomState(seed)

    # Shared base representation
    base = rng.randn(n_data, max(embed_dim_a, embed_dim_b))

    # Model A: base + noise
    rep_a = base[:, :embed_dim_a] + noise_level * rng.randn(n_data, embed_dim_a)

    if compatible:
        # Model B: base + small noise (compatible)
        rep_b = base[:, :embed_dim_b] + noise_level * rng.randn(n_data, embed_dim_b)
    else:
        # Model B: random (incompatible)
        rep_b = rng.randn(n_data, embed_dim_b)

    # Coverage: each model sees a subset
    coverage_a = np.zeros(n_data, dtype=bool)
    coverage_a[:n_data // 2] = True

    coverage_b = np.zeros(n_data, dtype=bool)
    coverage_b[n_data // 4:] = True

    model_a = ModelRepresentation(
        name=name_a,
        representations=rep_a,
        coverage=coverage_a,
    )
    model_b = ModelRepresentation(
        name=name_b,
        representations=rep_b,
        coverage=coverage_b,
    )

    return model_a, model_b
