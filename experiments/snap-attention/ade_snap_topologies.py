@dataclass
class ADETypeInfo:
    """Complete information about an ADE root system."""
    name: str
    rank: int
    dimension: int
    num_roots: int
    coxeter_number: int
    cartan_matrix: Matrix
    simple_roots: List
    dynkin_diagram: str
    mckay_group: Optional[str] = None
    platonic_solid: Optional[str] = None


def get_ade_info(ade_type: str) -> ADETypeInfo:
    """Extract complete ADE root system information using SymPy."""
    ct = CartanType(ade_type)
    
    # Get simple roots
    simple_roots = []
    for i in range(1, ct.rank() + 1):
        try:
            sr = ct.simple_root(i)
            simple_roots.append(sr)
        except:
            simple_roots.append(None)
    
    # Coxeter number
    h = COXETER_NUMBERS.get(ade_type, 'unknown')
    
    # Dynkin diagram description
    dynkin = _generate_dynkin_description(ade_type, ct.rank())
    
    # McKay correspondence (exceptional types only)
    mckay = MCKAY_CORRESPONDENCE.get(ade_type, {})
    
    return ADETypeInfo(
        name=ade_type,
        rank=ct.rank(),
        dimension=ct.dimension(),
        num_roots=NUM_ROOTS.get(ade_type, ct.roots()),
        coxeter_number=h,
        cartan_matrix=ct.cartan_matrix(),
        simple_roots=simple_roots,
        dynkin_diagram=dynkin,
        mckay_group=mckay.get('group'),
        platonic_solid=mckay.get('solid'),
    )


def _generate_dynkin_description(ade_type: str, rank: int) -> str:
    """Generate ASCII Dynkin diagram for an ADE type."""
    if ade_type.startswith('A'):
        nodes = '—'.join(['o'] * rank)
        return f"A{rank}: {nodes}"
    elif ade_type.startswith('D'):
        chain = '—'.join(['o'] * (rank - 1))
        return f"D{rank}: {chain}<o  (fork at node {rank-1})"
    elif ade_type == 'E6':
        return "E6: o—o—o—o—o\n         |\n         o"
    elif ade_type == 'E7':
        return "E7: o—o—o—o—o—o\n            |\n            o"
    elif ade_type == 'E8':
        return "E8: o—o—o—o—o—o—o\n               |\n               o"
    return f"{ade_type}: (diagram not rendered)"


# ============================================================================
# Snap Function on Root Lattices
# ============================================================================

def snap_to_root_lattice(
    point: np.ndarray,
    simple_roots: np.ndarray
) -> Tuple[np.ndarray, float]:
    """
    Snap a point to the nearest lattice point generated by simple roots.
    
    The root lattice L = {Σ n_i α_i | n_i ∈ ℤ} where α_i are simple roots.
    
    Args:
        point: Point to snap (in the ambient space)
        simple_roots: Matrix of simple roots (each row is a root)
    
    Returns:
        (nearest_lattice_point, delta_magnitude)
    """
    # Express point in terms of simple root coordinates
    # point ≈ Σ c_i α_i, solve for c_i
    # A^T A c = A^T point  (least squares projection onto root lattice)
    
    if simple_roots.shape[0] == 0:
        return np.zeros_like(point), np.linalg.norm(point)
    
    # Solve for coefficients
    ATA = simple_roots @ simple_roots.T
    ATp = simple_roots @ point
    
    try:
        coeffs = np.linalg.solve(ATA, ATp)
    except np.linalg.LinAlgError:
        coeffs = np.linalg.lstsq(simple_roots, point, rcond=None)[0]
    
    # Round to nearest integer coefficients
    int_coeffs = np.round(coeffs).astype(int)
    
    # Compute snapped point
    snapped = int_coeffs @ simple_roots
    
    # Delta = distance from snapped
    delta = np.linalg.norm(point - snapped)
    
    return snapped, delta


def compute_snap_quality(
    simple_roots: np.ndarray,
    num_samples: int = 10000,
    radius: float = 2.0
) -> Dict:
    """
    Compute snap quality metrics for a root lattice.
    
    Quality measures:
    - mean_delta: average distance from random points to snapped points
    - max_delta: worst-case snap distance (related to covering radius)
    - snap_variance: how much delta varies (isotropy measure)
    """
    dim = simple_roots.shape[1] if simple_roots.ndim > 1 else 1
    
    deltas = []
    for _ in range(num_samples):
        point = np.random.uniform(-radius, radius, size=simple_roots.shape[1])
        _, delta = snap_to_root_lattice(point, simple_roots)
        deltas.append(delta)
    
    deltas = np.array(deltas)
    
    return {
        'mean_delta': float(np.mean(deltas)),
        'std_delta': float(np.std(deltas)),
        'max_delta': float(np.max(deltas)),
        'median_delta': float(np.median(deltas)),
        'num_samples': num_samples,
    }


