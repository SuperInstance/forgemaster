# Coxeter Number Correlation Analysis
# ============================================================================

def analyze_coxeter_correlation() -> Dict:
    """
    Analyze the correlation between Coxeter number and snap quality
    across ADE types.
    
    From PLATONIC-SNAP-ADE.md: "r=-0.90 correlation with Coxeter numbers"
    
    We test: as Coxeter number increases, does snap quality change?
    """
    ade_types = ['A2', 'A3', 'D4', 'E6', 'E7', 'E8']
    
    results = []
    for t in ade_types:
        info = get_ade_info(t)
        
        # Convert SymPy simple roots to numpy
        np_roots = []
        for sr in info.simple_roots:
            if sr is not None:
                np_roots.append([float(N(x)) for x in sr])
        
        if np_roots:
            root_matrix = np.array(np_roots)
            
            # Pad to ensure consistent dimension
            max_dim = root_matrix.shape[1]
            
            # Compute snap quality
            quality = compute_snap_quality(root_matrix, num_samples=5000)
            
            results.append({
                'type': t,
                'rank': info.rank,
                'coxeter_number': info.coxeter_number,
                'num_roots': info.num_roots,
                'mean_delta': quality['mean_delta'],
                'max_delta': quality['max_delta'],
                'snap_variance': quality['std_delta'],
            })
    
    # Compute correlations
    if len(results) >= 3:
        coxeter_nums = [r['coxeter_number'] for r in results]
        mean_deltas = [r['mean_delta'] for r in results]
        max_deltas = [r['max_delta'] for r in results]
        num_roots_list = [r['num_roots'] for r in results]
        
        corr_coxeter_delta = np.corrcoef(coxeter_nums, mean_deltas)[0, 1]
        corr_roots_delta = np.corrcoef(num_roots_list, mean_deltas)[0, 1]
        corr_coxeter_max = np.corrcoef(coxeter_nums, max_deltas)[0, 1]
    else:
        corr_coxeter_delta = 0.0
        corr_roots_delta = 0.0
        corr_coxeter_max = 0.0
    
    return {
        'per_type': results,
        'corr_coxeter_mean_delta': float(corr_coxeter_delta),
        'corr_roots_mean_delta': float(corr_roots_delta),
        'corr_coxeter_max_delta': float(corr_coxeter_max),
    }


# ============================================================================
# Cartan Matrix Analysis
# ============================================================================

def analyze_cartan_matrices() -> Dict:
    """
    Analyze Cartan matrices for all ADE types.
    
    The Cartan matrix C_ij = 2(α_i, α_j)/(α_i, α_i) encodes the
    angular relationships between simple roots.
    
    For simply-laced types (A, D, E): C_ij ∈ {0, -1} for i≠j
    """
    ade_types = ['A2', 'A3', 'D4', 'E6', 'E7', 'E8']
    
    results = {}
    for t in ade_types:
        info = get_ade_info(t)
        cm = info.cartan_matrix
        
        # Convert to numpy for analysis
        cm_np = np.array(cm.tolist(), dtype=float)
        
        # Check simply-laced property: off-diagonal entries should be 0 or -1
        off_diag = cm_np.copy()
        np.fill_diagonal(off_diag, 0)
        is_simply_laced = np.all(np.isin(np.abs(off_diag[off_diag != 0]), [1]))
        
        # Determinant (related to the index of the root lattice)
        det = np.linalg.det(cm_np)
        
        # Eigenvalues (determine positive-definiteness)
        eigenvalues = np.linalg.eigvalsh(cm_np)
        
        # Condition number (numerical stability of snap function)
        cond = np.linalg.cond(cm_np)
        
        results[t] = {
            'rank': info.rank,
            'cartan_matrix': cm_np.tolist(),
            'simply_laced': bool(is_simply_laced),
            'determinant': float(det),
            'eigenvalues': eigenvalues.tolist(),
            'condition_number': float(cond),
            'coxeter_number': info.coxeter_number,
        }
    
    return results


