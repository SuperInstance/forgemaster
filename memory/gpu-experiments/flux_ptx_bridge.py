# ─── FLUX Register File ────────────────────────────────────────────

NUM_REGISTERS = 64  # FLUX has 64 registers

@dataclass
class FluxRegisterFile:
    """64-register file matching the FLUX VM spec."""
    regs: List[float]       # R0-R31: float registers
    iregs: List[int]        # R32-R47: int registers  
    addrs: List[int]        # R48-R63: address/pointer registers
    
    @staticmethod
    def create():
        return FluxRegisterFile(
            regs=[0.0] * 32,
            iregs=[0] * 16,
            addrs=[0] * 16
        )
    
    def get_float(self, idx: int) -> float:
        if idx < 32: return self.regs[idx]
        raise IndexError(f"R{idx} is not a float register")
    
    def set_float(self, idx: int, val: float):
        if idx < 32: self.regs[idx] = val
        else: raise IndexError(f"R{idx} is not a float register")
    
    def get_int(self, idx: int) -> int:
        if 32 <= idx < 48: return self.iregs[idx - 32]
        if idx < 32: return int(self.regs[idx])
        raise IndexError(f"R{idx} out of range")
    
    def get_addr(self, idx: int) -> int:
        if 48 <= idx < 64: return self.addrs[idx - 48]
        raise IndexError(f"R{idx} is not an address register")


# ─── Vector Operation Types ────────────────────────────────────────

class VectorOp(IntEnum):
    """FLUX ISA Vector/SIMD operations (0xB0-0xBF)"""
    VLOAD   = 0xB0   # Load vector from memory
    VSTORE  = 0xB1   # Store vector to memory
    VADD    = 0xB2   # Element-wise add
    VMUL    = 0xB3   # Element-wise multiply
    VDOT    = 0xB4   # Dot product (reduce)
    VNORM   = 0xB5   # L2 norm (reduce)
    VSCALE  = 0xB6   # Scale by scalar
    VMAXP   = 0xB7   # Element-wise max
    VMINP   = 0xB8   # Element-wise min
    VREDUCE = 0xB9   # Reduce with operation
    VGATHER = 0xBA   # Gather from indexed addresses
    VSCATTER= 0xBB   # Scatter to indexed addresses
    VSHUF   = 0xBC   # Shuffle lanes
    VMERGE  = 0xBD   # Merge by mask
    VCONF   = 0xBE   # Confidence propagation
    VSELECT = 0xBF   # Conditional select by confidence


