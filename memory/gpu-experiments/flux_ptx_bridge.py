# ─── ISA Bytecode Decoder ──────────────────────────────────────────

@dataclass
class DecodedInstruction:
    opcode: VectorOp
    rd: int       # destination register
    rs1: int      # source register 1
    rs2: int      # source register 2
    raw: bytes


def decode_flux_instruction(bytecode: bytes, pc: int) -> DecodedInstruction:
    """
    Decode a FLUX Format E instruction (4 bytes):
      [opcode:1] [rd:1] [rs1:1] [rs2:1]
    """
    if pc + 4 > len(bytecode):
        raise ValueError(f"PC {pc} out of bounds (len={len(bytecode)})")
    
    opcode_byte = bytecode[pc]
    rd = bytecode[pc + 1]
    rs1 = bytecode[pc + 2]
    rs2 = bytecode[pc + 3]
    
    try:
        op = VectorOp(opcode_byte)
    except ValueError:
        raise ValueError(f"Unknown vector opcode 0x{opcode_byte:02X} at PC {pc}")
    
    return DecodedInstruction(
        opcode=op,
        rd=rd,
        rs1=rs1,
        rs2=rs2,
        raw=bytecode[pc:pc+4]
    )


# ─── PTX Bridge: Compile ISA → CUDA ────────────────────────────────

class FluxPTXBridge:
    """
    Bridge between FLUX ISA bytecode and CUDA kernel execution.
    
    Compiles vector ISA ops (0xB0-0xBF) into CUDA kernels,
    manages device memory for register file, and launches kernels.
    """
    
    def __init__(self):
        self.rf = FluxRegisterFile.create()
        self.device_vectors = {}  # register_idx → device pointer
        self.vector_lengths = {}  # register_idx → length
        self.kernel_cache = {}    # (opcode, block_id) → compiled kernel
        self._block_counter = 0
        self._compiled_cu = ""
    
    def compile_block(self, instructions: List[DecodedInstruction]) -> str:
        """Compile a block of vector instructions into a single CUDA source."""
        self._block_counter += 1
        block_id = self._block_counter
        
        cu_parts = [
            "#include <cmath>\n",
            f"// FLUX ISA → CUDA Block {block_id}\n",
            f"// {len(instructions)} vector instructions compiled\n\n",
        ]
        
        for inst in instructions:
            template = KERNEL_TEMPLATES.get(inst.opcode)
            if template:
                cu_parts.append(template.format(block_id=block_id))
            else:
                cu_parts.append(f"// TODO: {inst.opcode.name} kernel not yet implemented\n")
        
        self._compiled_cu = "".join(cu_parts)
        return self._compiled_cu
    
    def emit_launch_code(self, instructions: List[DecodedInstruction], 
                          block_id: int) -> List[str]:
        """
        Generate the host-side kernel launch sequence.
        Returns list of C launch statements.
        """
        launches = []
        block_size = 256
        
        for inst in instructions:
            n = self.vector_lengths.get(inst.rs1, 0)
            if n == 0:
                n = self.vector_lengths.get(inst.rs2, 0)
            grid = max(1, (n + block_size - 1) // block_size)
            
            if inst.opcode == VectorOp.VLOAD:
                # VLOAD: rd = dest vec reg, rs1 = addr reg (source ptr), rs2 = int reg (length)
                launches.append(
                    f"flux_vload_{block_id}<<<{grid}, {block_size}>>>("
                    f"d_R{inst.rd}, d_mem_R{inst.rs1}, iR{inst.rs2});"
                )
            elif inst.opcode == VectorOp.VSTORE:
                launches.append(
                    f"flux_vstore_{block_id}<<<{grid}, {block_size}>>>("
                    f"d_mem_R{inst.rd}, d_R{inst.rs1}, iR{inst.rs2});"
                )
            elif inst.opcode == VectorOp.VADD:
                launches.append(
                    f"flux_vadd_{block_id}<<<{grid}, {block_size}>>>("
                    f"d_R{inst.rd}, d_R{inst.rs1}, d_R{inst.rs2}, {n});"
                )
            elif inst.opcode == VectorOp.VMUL:
                launches.append(
                    f"flux_vmul_{block_id}<<<{grid}, {block_size}>>>("
                    f"d_R{inst.rd}, d_R{inst.rs1}, d_R{inst.rs2}, {n});"
                )
            elif inst.opcode == VectorOp.VDOT:
                launches.append(
                    f"flux_vdot_{block_id}<<<{grid}, {block_size}>>>("
                    f"d_R{inst.rd}, d_R{inst.rs1}, d_R{inst.rs2}, {n});"
                )
            elif inst.opcode == VectorOp.VNORM:
                launches.append(
                    f"flux_vnorm_{block_id}<<<{grid}, {block_size}>>>("
                    f"d_R{inst.rd}, d_R{inst.rs1}, {n});"
                )
            elif inst.opcode == VectorOp.VSCALE:
                launches.append(
                    f"flux_vscale_{block_id}<<<{grid}, {block_size}>>>("
                    f"d_R{inst.rd}, d_R{inst.rs1}, scalar_R{inst.rs2}, {n});"
                )
            elif inst.opcode == VectorOp.VCONF:
                launches.append(
                    f"flux_vconf_{block_id}<<<{grid}, {block_size}>>>("
                    f"d_R{inst.rd}, d_R{inst.rs1}, d_R{inst.rs2}, {n});"
                )
            elif inst.opcode == VectorOp.VSELECT:
                launches.append(
                    f"flux_vselect_{block_id}<<<{grid}, {block_size}>>>("
                    f"d_R{inst.rd}, d_R{inst.rs1}, d_R{inst.rs2}, d_mask, threshold, {n});"
                )
            elif inst.opcode in (VectorOp.VGATHER, VectorOp.VSCATTER, 
                                  VectorOp.VSHUF, VectorOp.VMERGE, VectorOp.VREDUCE):
                launches.append(f"// TODO: launch for {inst.opcode.name}")
            else:
                launches.append(f"// Unsupported: {inst.opcode.name}")
        
        return launches
    
    def disassemble(self, bytecode: bytes) -> List[str]:
        """Disassemble FLUX vector instructions to human-readable form."""
        lines = []
        pc = 0
        while pc < len(bytecode):
            inst = decode_flux_instruction(bytecode, pc)
            lines.append(
                f"  {pc:04X}: {inst.raw.hex()} → {inst.opcode.name:8s} "
                f"R{inst.rd}, R{inst.rs1}, R{inst.rs2}"
            )
            pc += 4
        return lines


